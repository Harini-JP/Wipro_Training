package Day2;

public class Loopstatements {

	public static void main(String[] args) {
		 
		//FOR LOOP
		for (int i = 1; i <= 5; i++) {
	            System.out.println("i = " + i);
	}
		
		//WHILE LOOP
		int i=0;
		while(i<10) {
			 System.out.println(i);
			 i++;
			 System.out.println("Hello World");
			 
		}

}
}
