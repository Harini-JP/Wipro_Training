package Day1;

public class Strings {

	public static void main(String[] args) {
		
		//FINDING LENGTH
		String text="Swetha Priya";
		System.out.println(text.length());
		
		//STRING INDEXING
		System.out.println(text.indexOf("Priya"));
		
		//STRING CONCATENATION
		String first_name="Marry";
		String second_name="Alice";
		System.out.println(first_name + second_name);
		
		
		

	}

}
